enum class Direction
{
    LEFT,
    RIGHT,
    UP,
    DOWN,
    NONE
};

inline float Dir_to_Rota(Direction dir)
{
    switch (dir)
    {
    case Direction::UP:
        return 0.0f;
    case Direction::RIGHT:
        return 90.0f;
    case Direction::DOWN:
        return 180.0f;
    case Direction::LEFT:
        return 270.0f;
    default:
        return 0.0f;
    }
}

inline Direction ReverseDir(Direction dir)
{
    switch (dir)
    {
    case Direction::UP:
        return Direction::DOWN;
    case Direction::DOWN:
        return Direction::UP;
    case Direction::LEFT:
        return Direction::RIGHT;
    case Direction::RIGHT:
        return Direction::LEFT;
    default:
        return Direction::NONE;
    }
}

